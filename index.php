                                    <h2>ACERCA DEL SECADOR SOLAR</h2>
                                    <p style="text-align: justify;"><span>El secador solar o también conocido técnicamente como deshidratador solar tiene el objetivo de preservar frutas, verduras, semillas, hierbas e incluso carnes a través de distintos componentes.</span></p>
                                    <p style="text-align: justify;">Nuestro secador solar consta de varios componentes clave: el colector solar, que capta la energía del sol y la convierte en calor; la cámara de secado, donde se colocan los productos orgánicos en bandejas para su deshidratación; el sistema de ventilación, que facilita la circulación del aire caliente y la eliminación del aire húmedo, pudiendo ser natural o forzado; y la cubierta transparente, que permite la entrada de luz solar mientras retiene el calor dentro del secador.</p>
                                    <br>
                                    <p style="text-align: justify;">El secador solar está conectado a un sistema de monitoreo remoto que utiliza un microcontrolador de Espressif Systems con tecnología IoT. Este sistema es una aplicación web accesible desde internet, permite obtener un seguimiento de monitoreo de la temperatura y humedad en tiempo real. Asímismo, almacena los registros de los datos, facilitando el análisis del secado de productos vegetales y reduciendo la necesidad de la presencia física del operador.</p>
